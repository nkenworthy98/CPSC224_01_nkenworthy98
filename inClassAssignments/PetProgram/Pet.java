public class Pet
{
	private String name;
	private String animal;
	private int age;

	public void setName(String theName)
	{
		name = theName;
	}

	public void setAnimal(String theAnimal)
	{
		animal = theAnimal;
	}

	public void setAge(int theAge)
	{
		age = theAge;
	}

	public String getName()
	{
		return name;
	}

	public String getAnimal()
	{
		return animal;
	}

	public int getAge()
	{
		return age;
	}
}
